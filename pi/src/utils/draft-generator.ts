export function generateDraft(config: DraftConfig): string {
  const timestamp = new Date().toISOString();

  const formatList = (items?: string[]) => {
    if (!items || items.length === 0) return "- None specified";
    return items.map(item => `- ${item}`).join("\n");
  };

  return DRAFT_TEMPLATE
    .replace("{name}", config.name)
    .replace("{timestamp}", timestamp)
    .replace("{intelligence}", config.intent || config.request)
    // Business Plane
    .replace("{process}", formatList(config.business?.process))
    .replace("{rules}", formatList(config.business?.rules))
    // Data Plane
    .replace("{logic}", formatList(config.data?.logic))
    .replace("{state}", formatList(config.data?.state))
    // Control Plane
    .replace("{strategy}", formatList(config.control?.strategy))
    .replace("{distribution}", formatList(config.control?.distribution))
    // Foundation Plane
    .replace("{resource}", formatList(config.foundation?.resource))
    .replace("{abstraction}", formatList(config.foundation?.abstraction))
    // Observation Plane
    .replace("{obsData}", formatList(config.observation?.data))
    .replace("{analysis}", formatList(config.observation?.analysis))
    // Security Plane
    .replace("{identity}", formatList(config.security?.identity))
    .replace("{permissions}", formatList(config.security?.permissions))
    // Evolution Plane
    .replace("{time}", formatList(config.evolution?.time))
    .replace("{change}", formatList(config.evolution?.change))
    // Cross-cutting
    .replace("{crossPlaneDeps}", formatList(config.crossPlaneDependencies))
    .replace("{keyDecisions}", formatList(config.keyDecisions))
    .replace("{openQuestions}", formatList(config.openQuestions))
    .replace("{notes}", config.notes || "- Add notes here");
}

export function generateMinimalDraft(name: string, request: string): string {
  const analysis = analyzeRequest(request);

  return generateDraft({
    name,
    request,
    intent: analysis.intent,
    business: {
      process: analysis.businessProcess,
      rules: analysis.businessRules,
    },
    data: {
      logic: analysis.dataLogic,
      state: analysis.dataState,
    },
    control: {
      strategy: analysis.controlStrategy,
      distribution: analysis.controlDistribution,
    },
    foundation: {
      resource: analysis.foundationResource,
      abstraction: analysis.foundationAbstraction,
    },
    observation: {
      data: analysis.observationData,
      analysis: analysis.observationAnalysis,
    },
    security: {
      identity: analysis.securityIdentity,
      permissions: analysis.securityPermissions,
    },
    evolution: {
      time: analysis.evolutionTime,
      change: analysis.evolutionChange,
    },
    crossPlaneDependencies: [
      "- Business Plane → needs → Data Plane",
      "- Data Plane → feeds → Observation Plane",
      "- Control Plane → manages → Business Plane",
      "- Foundation Plane → supports → All Planes",
    ],
    keyDecisions: [
      "- Focus on high-impact planes first",
      "- Apply relevant planes only (not all 7 may apply)",
    ],
    openQuestions: [
      "- Review with QiaoChui for technical feasibility",
      "- Validate assumptions with stakeholders",
    ],
    notes: "- Ready for QiaoChui review",
  });
}


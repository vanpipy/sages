/**
 * Draft Generator - Creates MDD (Multi-Dimensional Design) drafts
 * Based on Fuxi's Seven Planes methodology
 */

export interface DraftConfig {
  name: string;
  request: string;
  intent?: string;
  // MDD Planes
  business?: {
    process?: string[];
    rules?: string[];
  };
  data?: {
    logic?: string[];
    state?: string[];
  };
  control?: {
    strategy?: string[];
    distribution?: string[];
  };
  foundation?: {
    resource?: string[];
    abstraction?: string[];
  };
  observation?: {
    data?: string[];
    analysis?: string[];
  };
  security?: {
    identity?: string[];
    permissions?: string[];
  };
  evolution?: {
    time?: string[];
    change?: string[];
  };
  crossPlaneDependencies?: string[];
  keyDecisions?: string[];
  openQuestions?: string[];
  notes?: string;
}


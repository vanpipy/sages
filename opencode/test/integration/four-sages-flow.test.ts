describe("LuBan - Task Execution with TDD", () => {
  let project: TestProject;
  let ctx: MockContext;

  beforeEach(() => {
    project = createTestProject();
    ctx = createMockContext(project.path);
  });

  afterEach(() => {
    cleanupTestProject(project);
  });

  it("should acquire file locks before task execution", () => {
    const lockRegistry: Record<string, { taskId: string; lockedBy: string; lockedAt: string }> = {};
    
    function acquireLock(filePath: string, taskId: string): boolean {
      const key = `${taskId}:${filePath}`;
      if (lockRegistry[key]) return false;
      lockRegistry[key] = {
        taskId,
        lockedBy: taskId,
        lockedAt: new Date().toISOString()
      };
      return true;
    }

    // T1 acquires lock on src/index.ts
    expect(acquireLock("src/index.ts", "T1")).toBe(true);
    
    // T2 cannot acquire same file - same key "T2:src/index.ts" doesn't exist yet
    // But T1 already has "T1:src/index.ts" - different key
    // So we need to check differently - the actual lock should be checked by file path
    const locksByFile: Record<string, string> = {}; // filePath -> taskId
    
    function acquireLockWithConflictDetection(filePath: string, taskId: string): { success: boolean; conflict?: string } {
      if (locksByFile[filePath] && locksByFile[filePath] !== taskId) {
        return { success: false, conflict: locksByFile[filePath] };
      }
      locksByFile[filePath] = taskId;
      return { success: true };
    }

    expect(acquireLockWithConflictDetection("src/index.ts", "T1").success).toBe(true);
    expect(acquireLockWithConflictDetection("src/index.ts", "T2").success).toBe(false); // Already locked by T1
    expect(acquireLockWithConflictDetection("src/index.ts", "T2").conflict).toBe("T1");
    expect(acquireLockWithConflictDetection("src/utils.ts", "T2").success).toBe(true); // Different file
  });

  it("should track task execution state", () => {
    interface TaskState {
      taskId: string;
      status: "pending" | "in_progress" | "completed" | "failed";
      startedAt?: string;
      completedAt?: string;
    }

    const taskStates: Map<string, TaskState> = new Map();

    // Start task
    taskStates.set("T1", { taskId: "T1", status: "in_progress", startedAt: new Date().toISOString() });
    expect(taskStates.get("T1")?.status).toBe("in_progress");

    // Complete task
    const t1 = taskStates.get("T1")!;
    taskStates.set("T1", { ...t1, status: "completed", completedAt: new Date().toISOString() });
    expect(taskStates.get("T1")?.status).toBe("completed");
  });

  it("should respect TDD cycle - test before implementation", () => {
    let redPhaseRan = false;
    let greenPhaseRan = false;

    // RED: Write failing test first
    function writeFailingTest(): void {
      redPhaseRan = true;
      // Test should fail initially
    }

    // GREEN: Write minimal code to pass
    function writeMinimalCode(): void {
      if (!redPhaseRan) throw new Error("Must write test first");
      greenPhaseRan = true;
    }

    // Execute TDD cycle
    writeFailingTest();
    writeMinimalCode();

    expect(redPhaseRan).toBe(true);
    expect(greenPhaseRan).toBe(true);
  });

  it("should release locks after task completion", () => {
    const locks: Set<string> = new Set(["T1:src/index.ts", "T1:src/auth.ts"]);

    function releaseTaskLocks(taskId: string): number {
      let released = 0;
      for (const key of locks) {
        if (key.startsWith(`${taskId}:`)) {
          locks.delete(key);
          released++;
        }
      }
      return released;
    }

    expect(releaseTaskLocks("T1")).toBe(2);
    expect(locks.size).toBe(0);
  });
});


describe("GaoYao - Quality Audit", () => {
  let project: TestProject;
  let ctx: MockContext;

  beforeEach(() => {
    project = createTestProject();
    ctx = createMockContext(project.path);
  });

  afterEach(() => {
    cleanupTestProject(project);
  });

  it("should run quick review for CRITICAL issues only", () => {
    const quickReviewChecks = [
      "syntax_errors",
      "import_errors", 
      "type_errors",
      "security_vulnerabilities_critical"
    ];

    expect(quickReviewChecks).toHaveLength(4);
  });

  it("should run full review for all quality gates", () => {
    const fullReviewChecks = [
      "code_quality",
      "security_vulnerabilities",
      "test_coverage",
      "performance",
      "documentation",
      "style",
      "linting"
    ];

    expect(fullReviewChecks).toHaveLength(7);
  });

  it("should detect security vulnerabilities", () => {
    const vulnerablePatterns = [
      { pattern: "eval(", severity: "critical", reason: "Code injection" },
      { pattern: "SELECT * FROM", severity: "major", reason: "SQL injection risk" },
      { pattern: "password = ", severity: "critical", reason: "Hardcoded credential" },
      { pattern: "exec(", severity: "critical", reason: "Command injection" }
    ];

    const code = "const query = 'SELECT * FROM users'; eval(userInput);";
    
    const detected: string[] = [];
    for (const { pattern } of vulnerablePatterns) {
      if (code.includes(pattern)) {
        detected.push(pattern);
      }
    }

    expect(detected).toContain("SELECT * FROM");
    expect(detected).toContain("eval(");
  });

  it("should return PASS verdict for quality code", () => {
    interface ReviewResult {
      verdict: "PASS" | "NEEDS_CHANGES" | "REJECTED";
      qualityScore: number;
      checks: Record<string, boolean>;
    }

    const cleanCode: ReviewResult = {
      verdict: "PASS",
      qualityScore: 95,
      checks: {
        codeQuality: true,
        security: true,
        testCoverage: true,
        performance: true,
        documentation: true
      }
    };

    expect(cleanCode.verdict).toBe("PASS");
    expect(cleanCode.qualityScore).toBeGreaterThanOrEqual(90);
  });

  it("should return NEEDS_CHANGES for code with issues", () => {
    interface ReviewResult {
      verdict: "PASS" | "NEEDS_CHANGES" | "REJECTED";
      qualityScore: number;
      issues: string[];
    }

    const codeWithIssues: ReviewResult = {
      verdict: "NEEDS_CHANGES",
      qualityScore: 65,
      issues: [
        "Missing error handling in auth.ts",
        "No test coverage for session module",
        "Performance: N+1 query in user lookup"
      ]
    };

    expect(codeWithIssues.verdict).toBe("NEEDS_CHANGES");
    expect(codeWithIssues.issues.length).toBeGreaterThan(0);
  });
});

